mid				BIGINT			PRIMARY KEY AUTO_INCREMENT,
businessNumber	VARCHAR(13)		NOT NULL,
longitude		DOUBLE			NOT NULL,
latitude		DOUBLE			NOT NULL,
CONSTRAINT map_FK
	FOREIGN KEY(businessNumber) REFERENCES shop(businessNumber)
)

